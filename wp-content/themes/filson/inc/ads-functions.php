function hexwp_ads() {
	global $hexwp_data;
	if( !empty($hexwp_data[ 'newwindow' ]) ){ $newwindow = '_blank'; }else{$newwindow= '';}
	if( !empty($hexwp_data[ 'nofollow' ]) ){ $nofollow = 'nofollow'; }else{$nofollow= '';}
    $image = isset( $hexwp_data[ 'image' ] ) ? $hexwp_data[ 'image' ]: '';
 	$ads_url = isset( $hexwp_data[ 'ads_url' ] ) ? $hexwp_data[ 'ads_url' ]: '';
	if( !empty($hexwp_data[ 'resize' ]) ){ $resize = 'hw-resize'; }else{$resize= '';}
      echo  '<div class="hw-ads '.esc_attr($resize).'">';
     	echo '<a href="'.esc_url($ads_url).'"   rel="'.esc_attr($nofollow).'" target="'.esc_attr($newwindow).'">';
    		echo '<img alt="ads" src="'.esc_url($image).'" />';
  		echo '</a>'; 		
 	echo '</div>'  ;  
}

