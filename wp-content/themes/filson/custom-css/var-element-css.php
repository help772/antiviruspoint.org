/*****************************************************************************************************************************************************
******************************************************************************************************************************************************
 
																	Padding
 
*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

if ( !function_exists ( "hexwp_var_padding" )){
function hexwp_var_padding( $name=false,$option=false,$id=false) {
	$css="";
	if(!empty($option[$id]['top']) || !empty($option[$id]['left']) || !empty($option[$id]['bottom']) || !empty($option[$id]['right']) ){
		$padding_unit = !empty($option[$id]['unit']) ?$option[$id]['unit'] : 'px';
		$padding_top = intval(isset($option[$id]['top'])) ?  $option[$id]['top'].$padding_unit : '0';
		$padding_left = intval(isset($option[$id]['left'])) ? $option[$id]['left'].$padding_unit : '0';
		$padding_bottom = intval(isset($option[$id]['bottom'])) ? $option[$id]['bottom'].$padding_unit  : '0';
		$padding_right = intval(isset($option[$id]['right'])) ? $option[$id]['right'].$padding_unit : '0';	
		if(is_rtl()){
 		$css.=$name.':'.$padding_top.' '.$padding_right.' '.$padding_bottom.' '.$padding_left.';';
		}else{
 		$css.=$name.':'.$padding_top.' '.$padding_left.' '.$padding_bottom.' '.$padding_right.';';
		}
	
	}
	return $css;
}
}
/*****************************************************************************************************************************************************
******************************************************************************************************************************************************
 
																	Element Font Typo Css
 
*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
function hexwp_var_font_typo( $name=false,$option=false,$id =false) {
	$css="";
	if(!empty($option[$id]['font_size'])){
 		$css.= intval(isset($option[$id]['font_size'])) ? $name.'-fn-sz:'.$option[$id]['font_size'].'px;': ' ';
	}
	if(!empty($option[$id]['font_weight'])){
		$css.= $name.'-fn-wt:'.$option[$id]['font_weight'].';';
	}	
	if(!empty($option[$id]['text_transform'])){
 		$css.=  $name.'-fn-tr:'.$option[$id]['text_transform'].';';
 	}	
	if(!empty($option[$id]['font_style'])){
 		$css.=    $name.'-fn-st:'.$option[$id]['font_style'].';';
 	}		
	return $css;	
}
/*****************************************************************************************************************************************************
******************************************************************************************************************************************************
 
																	Element Gradient Background Color Css
 
*/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////	
function hexwp_var_gradient_background_color_css( $name=false,$option=false,$id=false,$id_2=false,$deg='to Right') {
	$css='';
		if(!empty($option[$id])){
  			$css = $name.': '.esc_html($option[$id]).';';
 			if(!empty($option[$id_2])){
 				$css =  $name.': linear-gradient('.$deg.', '.$option[$id].' 0%,'.$option[$id_2].' 100%) ;';
 			}
	}
		
	return $css;
 
}